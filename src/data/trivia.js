export function getRandomTrivia(currentArc, triviaHistory = []) {
  // Lọc câu hỏi theo Arc
  let eligible = TRIVIA_DATA.filter(t => t.arcs.includes(currentArc))
  
  // Lọc bỏ những câu đã thi
  let unasked = eligible.filter(t => !triviaHistory.includes(t.id))
  
  // Nếu đã thi hết câu của Arc này, fallback lấy toàn bộ các câu của Arc đó để thi lại
  if (unasked.length === 0) {
    unasked = eligible
  }
  
  // Nếu vẫn không có (fallback cực đoan), lấy bừa 1 câu bất kỳ trong toàn bộ data
  if (unasked.length === 0) {
    unasked = TRIVIA_DATA
  }

  const randomIndex = Math.floor(Math.random() * unasked.length)
  return unasked[randomIndex]
}
